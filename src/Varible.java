public class Varible {
    // 변수 : 값을 갖는 상자
    // 값 : 메모리에 저장된 데이터 조각
    public static void main(String[] args){
        int num; // int형 변수 num 선언
        num = 100; // num 변수에 값 100을 대입, 할당
        System.out.println(num); // num 출력

        int num2 = 200; // 변수 선언과 동시에 값을 대입함
        System.out.println(num2);
    }
}
